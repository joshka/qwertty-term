/// Look up a `Key` by its Zig `snake_case` tag name (the inverse of
/// [`tag_name`]). Used by [`Key::fromW3C`].
fn key_from_tag_name(name: &str) -> Option<Key> {
    ALL.iter().copied().find(|&k| tag_name(k) == name)
}

