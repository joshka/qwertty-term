impl Key {
    /// Converts an ASCII character to a key, if possible. This returns
    /// `None` if the character is unknown.
    ///
    /// Note that this can't distinguish between physical keys, i.e. '0'
    /// may be from the number row or the keypad, but it always maps
    /// to `Digit0`.
    ///
    /// This is what we want, we want people to create keybindings that
    /// are independent of the physical key.
    ///
    /// Port of `Key.fromASCII`.
    pub fn from_ascii(ch: u8) -> Option<Key> {
        for &(codepoint, key) in CODEPOINT_MAP {
            // No ASCII characters should ever map to a keypad key.
            if key.keypad() {
                continue;
            }
            if codepoint == ch as u32 {
                return Some(key);
            }
        }
        None
    }

    /// Converts a W3C key code to a Ghostty key enum value.
    ///
    /// All required W3C key codes are supported, but there are a number of
    /// non-standard key codes that are not supported. In the case the value
    /// is invalid or unsupported, this function will return `None`.
    ///
    /// Port of `Key.fromW3C`. The Zig version first tries a direct
    /// lowercase match against the tag names (this alone handles codes
    /// like `"F1"` -> `f1`, `"Enter"` -> `enter`), then falls back to
    /// converting `FooBar` to `foo_bar` (uppercase letters/digits other
    /// than the first character start a new `_`-separated segment).
    pub fn from_w3c(code: &str) -> Option<Key> {
        if code.len() > 128 {
            return None;
        }

        // First just check the whole thing lowercased, this is the simple
        // case (e.g. "Enter" -> "enter", "F1" -> "f1").
        let lower = code.to_ascii_lowercase();
        if let Some(key) = key_from_tag_name(&lower) {
            return Some(key);
        }

        // We need to convert FooBar to foo_bar.
        let mut buf = String::with_capacity(code.len() + 8);
        for (i, ch) in code.chars().enumerate() {
            match ch {
                'a'..='z' => buf.push(ch),
                // Caps and numbers trigger underscores.
                'A'..='Z' | '0'..='9' => {
                    if i > 0 {
                        buf.push('_');
                    }
                    buf.push(ch.to_ascii_lowercase());
                }
                // We don't know of any key codes that aren't alphanumeric.
                _ => return None,
            }
        }

        key_from_tag_name(&buf)
    }

    /// Converts a Ghostty key enum value to a W3C key code.
    ///
    /// Port of `Key.w3c`. The Zig version uses `inline for`/`@tagName`
    /// comptime introspection to turn the tag name into UpperCamelCase at
    /// compile time; Rust has no such reflection, so this is a small
    /// runtime string transform over [`tag_name`] instead (uppercase the
    /// first character, and the character following each `_`, dropping
    /// the `_`). This mirrors the Zig algorithm exactly and is proven to
    /// be the exact inverse of [`from_w3c`](Key::from_w3c) for every
    /// variant by the `w3c` test below.
    pub fn w3c(self) -> String {
        let name = tag_name(self);
        let mut out = String::with_capacity(name.len());
        let mut chars = name.chars().peekable();
        let mut first = true;
        while let Some(ch) = chars.next() {
            if first {
                out.extend(ch.to_uppercase());
                first = false;
            } else if ch == '_' {
                if let Some(next_ch) = chars.next() {
                    out.extend(next_ch.to_uppercase());
                }
            } else {
                out.push(ch);
            }
        }
        out
    }

    /// True if this key represents a printable character.
    ///
    /// Port of `Key.printable`.
    pub fn printable(self) -> bool {
        CODEPOINT_MAP.iter().any(|&(_, key)| key == self)
    }

    /// True if this key is a modifier.
    ///
    /// Port of `Key.modifier`.
    pub fn modifier(self) -> bool {
        matches!(
            self,
            Key::ShiftLeft
                | Key::ControlLeft
                | Key::AltLeft
                | Key::MetaLeft
                | Key::ShiftRight
                | Key::ControlRight
                | Key::AltRight
                | Key::MetaRight
        )
    }

    /// Whether this key should be remappable by the operating system.
    ///
    /// On certain OSes (namely Linux and the BSDs) certain keys like the
    /// functional keys are expected to be remappable by the user, such as
    /// in the very common use case of swapping the Caps Lock key with the
    /// Escape key with the XKB option `caps:swapescape`.
    ///
    /// However, the way XKB implements this is by essentially acting as a
    /// software key remapper that destroys all information about the
    /// original physical key, leading to very annoying bugs like #7309
    /// where the physical key `XKB_KEY_c` gets remapped into
    /// `XKB_KEY_Cyrillic_tse`, which causes all of our physical key
    /// handling to completely break down. _Very naughty._
    ///
    /// As a compromise, given that writing system keys (§3.1.1) comprise
    /// the majority of keys that "change meaning [...] based on the
    /// current locale and keyboard layout", we allow all other keys to be
    /// remapped by default since they should be fairly harmless. We might
    /// consider making this configurable, but for now this should at
    /// least placate most people.
    ///
    /// Port of `Key.shouldBeRemappable`.
    pub fn should_be_remappable(self) -> bool {
        !matches!(
            self,
            // "Writing System Keys" § 3.1.1
            Key::Backquote
                | Key::Backslash
                | Key::BracketLeft
                | Key::BracketRight
                | Key::Comma
                | Key::Digit0
                | Key::Digit1
                | Key::Digit2
                | Key::Digit3
                | Key::Digit4
                | Key::Digit5
                | Key::Digit6
                | Key::Digit7
                | Key::Digit8
                | Key::Digit9
                | Key::Equal
                | Key::IntlBackslash
                | Key::IntlRo
                | Key::IntlYen
                | Key::KeyA
                | Key::KeyB
                | Key::KeyC
                | Key::KeyD
                | Key::KeyE
                | Key::KeyF
                | Key::KeyG
                | Key::KeyH
                | Key::KeyI
                | Key::KeyJ
                | Key::KeyK
                | Key::KeyL
                | Key::KeyM
                | Key::KeyN
                | Key::KeyO
                | Key::KeyP
                | Key::KeyQ
                | Key::KeyR
                | Key::KeyS
                | Key::KeyT
                | Key::KeyU
                | Key::KeyV
                | Key::KeyW
                | Key::KeyX
                | Key::KeyY
                | Key::KeyZ
                | Key::Minus
                | Key::Period
                | Key::Quote
                | Key::Semicolon
                | Key::Slash
        )
    }

    /// Returns true if this is a keypad key.
    ///
    /// Port of `Key.keypad`.
    pub fn keypad(self) -> bool {
        tag_name(self).starts_with("numpad_")
    }

    /// Returns the codepoint representing this key, or `None` if the key
    /// is not printable.
    ///
    /// For keys with two `CODEPOINT_MAP` entries (like `Digit0`/`Numpad0`
    /// sharing `'0'`), this returns the first match in table order, i.e.
    /// the non-keypad one, since keypad entries are last in the table.
    ///
    /// Port of `Key.codepoint`.
    pub fn codepoint(self) -> Option<u32> {
        CODEPOINT_MAP
            .iter()
            .find(|&&(_, key)| key == self)
            .map(|&(codepoint, _)| codepoint)
    }

    /// True if this key is one of the left or right versions of super
    /// (macOS) or ctrl.
    ///
    /// Port of `Key.ctrlOrSuper`. The Zig version branches on
    /// `builtin.target.os.tag.isDarwin()` at comptime (so only one branch
    /// is ever compiled in). We use a runtime `cfg!(target_os = "macos")`
    /// check instead of `#[cfg(...)]`-gated functions so both branches
    /// are visible to `cargo test`/clippy on every host (the branch
    /// actually taken still depends on the host OS, matching Zig's
    /// per-platform-build behavior).
    pub fn ctrl_or_super(self) -> bool {
        if cfg!(target_os = "macos") {
            self == Key::MetaLeft || self == Key::MetaRight
        } else {
            self == Key::ControlLeft || self == Key::ControlRight
        }
    }

    /// True if this key is either left or right shift.
    ///
    /// Port of `Key.leftOrRightShift`.
    pub fn left_or_right_shift(self) -> bool {
        self == Key::ShiftLeft || self == Key::ShiftRight
    }

    /// True if this key is either left or right alt.
    ///
    /// Port of `Key.leftOrRightAlt`.
    pub fn left_or_right_alt(self) -> bool {
        self == Key::AltLeft || self == Key::AltRight
    }
}

/// This sets either "ctrl" or "super" to true (but not both) on mods
/// depending on if the build target is Mac or not. On Mac, we default to
/// super (i.e. super+c for copy) and on non-Mac we default to ctrl (i.e.
/// ctrl+c for copy).
///
/// Port of the module-level `ctrlOrSuper` free function. See
/// [`Key::ctrl_or_super`] for why this uses a runtime `cfg!()` check.
pub fn ctrl_or_super(mods: Mods) -> Mods {
    let mut copy = mods;
    if cfg!(target_os = "macos") {
        copy.super_ = true;
    } else {
        copy.ctrl = true;
    }
    copy
}

