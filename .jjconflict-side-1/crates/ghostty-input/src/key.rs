//! Key input primitives (port of `input/key.zig`).
//!
//! Ported: [`KeyEvent`], [`Action`], and the [`Key`] enum with its
//! `fromASCII`/`fromW3C`/`w3c`/`printable`/`modifier`/`shouldBeRemappable`/
//! `keypad`/`codepoint`/`ctrlOrSuper`/`leftOrRightShift`/`leftOrRightAlt`
//! methods, plus the module-level `ctrl_or_super` free function.
//!
//! Not ported: `imguiKey()` (lines 544-737 of the Zig source). It maps
//! `Key` variants to `cimgui` ImGui key constants for the debug UI only,
//! which has no equivalent in this headless, freestanding crate.
use crate::key_mods::Mods;

/// A generic key input event. This is the information that is necessary
/// regardless of apprt in order to generate the proper terminal
/// control sequences for a given key press.
///
/// Some apprts may not be able to provide all of this information, such
/// as GLFW. In this case, the apprt should provide as much information
/// as it can and it should be expected that the terminal behavior
/// will not be totally correct.
///
/// Port of `KeyEvent`.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct KeyEvent {
    /// The action: press, release, etc.
    pub action: Action,

    /// The keycode of the physical key that was pressed. This is agnostic
    /// to the layout. Layout-dependent matching can only be done via the
    /// UTF-8 or unshifted codepoint.
    pub key: Key,

    /// Mods are the modifiers that are pressed.
    pub mods: Mods,

    /// The mods that were consumed in order to generate the text
    /// in utf8. This has the mods set that were consumed, so to
    /// get the set of mods that are effective you must negate
    /// mods with this.
    ///
    /// This field is meaningless if utf8 is empty.
    pub consumed_mods: Mods,

    /// Composing is true when this key event is part of a dead key
    /// composition sequence and we're in the middle of it.
    pub composing: bool,

    /// The utf8 sequence that was generated by this key event.
    /// This will be an empty string if there is no text generated.
    /// If composing is true and this is non-empty, this is preedit
    /// text.
    ///
    /// The Zig field is `[]const u8`; we use an owned `String` here since
    /// callers construct `KeyEvent`s directly (there's no borrow-checker
    /// win to be had from `Cow`/`&str` given the value is usually short
    /// and freshly built per key event).
    pub utf8: String,

    /// The codepoint for this key when it is unshifted. For example,
    /// shift+a is "A" in UTF-8 but unshifted would provide 'a'.
    ///
    /// The Zig field is `u21`; Rust has no 21-bit integer type, so this
    /// is a `u32` with the valid range documented as `0..=0x1FFFFF`
    /// (i.e. any Unicode scalar value's codepoint, or `0` for "none").
    pub unshifted_codepoint: u32,
}

impl Default for KeyEvent {
    fn default() -> Self {
        KeyEvent {
            action: Action::Press,
            key: Key::Unidentified,
            mods: Mods::default(),
            consumed_mods: Mods::default(),
            composing: false,
            utf8: String::new(),
            unshifted_codepoint: 0,
        }
    }
}

impl KeyEvent {
    /// Returns the effective modifiers for this event. The effective
    /// modifiers are the mods that should be considered for keybindings.
    ///
    /// Port of `KeyEvent.effectiveMods`.
    pub fn effective_mods(&self) -> Mods {
        if self.utf8.is_empty() {
            return self.mods;
        }
        self.mods.unset(self.consumed_mods)
    }

    /// Returns a unique hash for this key event to be used for tracking
    /// uniqueness specifically with bindings. This omits fields that are
    /// irrelevant for bindings.
    ///
    /// The Zig version uses `std.hash.Wyhash`. We use
    /// `std::collections::hash_map::DefaultHasher` instead: its algorithm
    /// is *not* guaranteed to be stable across Rust versions or
    /// platforms, but that's fine here since binding-hash comparisons
    /// only ever happen within a single running process (e.g. comparing
    /// two hashes computed moments apart), never persisted or compared
    /// cross-process.
    ///
    /// Port of `KeyEvent.bindingHash`.
    pub fn binding_hash(&self) -> u64 {
        use std::collections::hash_map::DefaultHasher;
        use std::hash::{Hash, Hasher};

        let mut hasher = DefaultHasher::new();

        // These are all the fields that are explicitly part of Trigger.
        self.key.hash(&mut hasher);
        self.unshifted_codepoint.hash(&mut hasher);
        self.mods.binding().hash(&mut hasher);

        // Notes on unmapped things and why:
        //
        // - action: we don't have action-specific bindings right now
        //   AND we want to know if a key resulted in a binding regardless
        //   of action because a press should also ignore a release and so
        //   on.
        //
        // We can add to this if there is other confusion.

        hasher.finish()
    }
}

/// The action associated with an input event.
///
/// In Zig this is backed by a `c_int` so that the enum can be used as-is
/// for the embedding API.
///
/// IMPORTANT: Any changes here update `include/ghostty.h`.
///
/// Port of `Action`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Action {
    Release,
    Press,
    Repeat,
}

